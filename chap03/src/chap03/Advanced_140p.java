package chap03;

public class Advanced_140p {

	public static void main(String[] args) {
		
		for (int i = 2; i <= 9; i++)
		{
			for (int k = 1; k <= 9; k++)
			{
				System.out.println(i + " x " + k + " = " + (k * i) );
				System.out.println();
			}
			
			System.out.println();
			System.out.println();
		}
	}
}